void timer(struct regs *r) {
	ticks++;
	if (!(ticks%2)) {
		char* buf = wf_malloc(8);
		itoa(ticks, buf, 10);

		vga_kputs(buf, 150, 0);
	}

	if (!(ticks % 100)) {
		kprintx("ESP", r->esp);
		kprintx("EIP", r->eip);
	}

}


// Disable interrupts
