static mutex key_mutex = { .lock = 0 };

extern STREAM* kb;

void event_loop() {
	while(1) {

		acquire(&key_mutex);
		if (ftell(kb)) {
			fflush(kb);
			//list_procs();

		}
		release(&key_mutex);

		yield();
	}
}


